        public static string GetColorByTeam(string constructorid)
        {
            switch (constructorid)
            {
                case jaguar:
                    return "#0c8f00"; // Verde marcado
                case tororosso:
                    return "#0033ff"; // Azul marcado
                case benetton:
                    return "#00bfff"; // Azul muy claro
                case williams:
                    return "#ffffff"; // Blanco
                case alfa:
                    return "#780002"; // Rojo muy oscuro
                case redbull:
                    return "#001b66"; // Azul muy oscuro
                case racingpoint:
                case forceindia:
                    return "#ff69f0"; // Rosa 
                case haas:
                case lotusf1:
                case lotusborgward:
                case lotusbrm:
                case lotusclimax:
                case lotusford:
                case lotusmaserati:
                case lotuspw:
                case lotusracing:
                    return "#543e00"; // Negro con amarillo
                case ferrari: return "#ff0000";  // Rojo
                case mercedes: return "#00dee6"; // Azul turquesa
                case minardi: return "#212121"; // Negro no muy oscuro
                case renualt: return "#ffee00"; // Amarillo
                case mclaren: return "#ff6600"; // Naranja
                default:
                    var random = new Random();
                    return String.Format("#{0:X6}", random.Next(0x1000000));
            }
        }

